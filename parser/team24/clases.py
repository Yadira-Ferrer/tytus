    def __init__(self,  uno) :
        self.uno = uno

    def __init__(self,  uno,dos) :
        self.uno = uno
        self.dos = dos

    def __init__(self,  uno,dos,tres) :
        self.uno = uno
        self.dos = dos
        self.tres = tres
    
    def __init__(self,  uno,dos,tres) :
        self.uno = uno
        self.dos = dos
        self.tres = tres
    
    def __init__(self,  uno,dos,tres,cuatro) :
        self.uno = uno
        self.dos = dos
        self.tres = tres
        self.cuatro= cuatro

    def __init__(self,  uno,dos,tres,cuatro, cinco) :
        self.uno = uno
        self.dos = dos
        self.tres = tres
        self.cuatro= cuatro
        self.cinco= cinco

    def __init__(self,  uno,dos,tres,cuatro, cinco,seis) :
        self.uno = uno
